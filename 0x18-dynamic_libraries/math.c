#include "math.h"
/**
 * add - calc add
 * @a:P1
 * @b:P2
 * return:int
 */
long int add(int a, int b)
{
	return(a + b);
}
/**
 * sub - calc sub
 * @a:P1
 * @b:P2
 * return:int
 */
long int sub(int a, int b)
{
	return(a - b);
}
/**
 * mul - calc mul
 * @a:P1
 * @b:P2
 * return:int
 */
double mul(int a, int b)
{
	return(a * b);
}
/**
 * div - calc div
 * @a:P1
 * @b:P2
 * return:int
 */
unsigned int div(int a, int b)
{
	return(a / b);
}
/**
 * mod - calc mod
 * @a:P1
 * @b:P2
 * return:int
 */
unsigned int mod(int a, int b)
{
	return(a % b);
	
}
